def _clamp_width(mob, max_w: float = MAX_WIDTH):
    """Scale mob down if wider than max_w. Returns mob (mutated in-place)."""
    if mob.width > max_w:
        mob.scale(max_w / mob.width)
    return mob


def _zone_b_positions(n_lines: int) -> list:
    """
    Return a list of Y-positions for n_lines in Zone B,
    stacked downward from ZONE_B_TOP_Y with BUILD_SPACING between them.
    Lines are centered horizontally.
    """
    positions = []
    for i in range(n_lines):
        positions.append(ZONE_B_TOP_Y - i * BUILD_SPACING)
    return positions


