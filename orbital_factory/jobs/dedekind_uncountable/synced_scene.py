def _build_sequence_list(cfg: dict) -> VGroup:
    """
    Show a sequence like  r₁, r₂, r₃, ...  with optional highlighting.
    cfg keys:
        items:     list of str (LaTeX)  e.g. ["r_1", "r_2", "r_3", "\\dots"]
        highlight: list of int (indices to highlight)
        label:     str (optional label above)
    """
    items     = cfg.get("items", ["r_1", "r_2", "r_3", "\\dots"])
    highlight = cfg.get("highlight", [])
    label     = cfg.get("label", "")

    parts = []
    for i, item in enumerate(items):
        color = SEQ_HIGHLIGHT if i in highlight else WHITE
        m = MathTex(item, color=color, font_size=40)
        parts.append(m)
        if i < len(items) - 1:
            comma = MathTex(",", color=GREY, font_size=40)
            parts.append(comma)

    row = VGroup(*parts).arrange(RIGHT, buff=0.2)

    group = VGroup(row)
    if label:
        lbl = Tex(label, color=LABEL_COLOR, font_size=32)
        lbl.next_to(row, UP, buff=0.4)
        group.add(lbl)

    group.scale(0.85)
    return group


def _build_table(cfg: dict) -> VGroup:
    """
    Simple table/grid.
    cfg keys:
        headers: list of str
        rows:    list of list of str
        highlight_cells: list of [row, col] to highlight
    """
    headers = cfg.get("headers", [])
    rows    = cfg.get("rows", [])
    hl      = cfg.get("highlight_cells", [])

    all_rows = []
    if headers:
        all_rows.append(headers)
    all_rows.extend(rows)

    # Build as MathTable
    entries = []
    for r, row in enumerate(all_rows):
        row_entries = []
        for c, cell in enumerate(row):
            m = MathTex(cell, font_size=30)
            # Highlight header row
            if r == 0 and headers:
                m.set_color(LABEL_COLOR)
            # Highlight specific cells
            for hr, hc in hl:
                adj_r = hr + (1 if headers else 0)
                if r == adj_r and c == hc:
                    m.set_color(SEQ_HIGHLIGHT)
            row_entries.append(m)
        entries.append(row_entries)

    # Use VGroup grid layout
    group = VGroup()
    cell_w = 1.4
    cell_h = 0.6
    for r, row in enumerate(entries):
        for c, entry in enumerate(row):
            x = (c - len(row) / 2 + 0.5) * cell_w
            y = -r * cell_h
            entry.move_to([x, y, 0])
            bg = Rectangle(
                width=cell_w - 0.05, height=cell_h - 0.05,
                fill_color=TABLE_HEADER if (r == 0 and headers) else TABLE_CELL,
                fill_opacity=0.4,
                stroke_color=GREY_D, stroke_width=1,
            )
            bg.move_to(entry.get_center())
            group.add(bg, entry)

    group.scale(0.8)
    return group


