                show_slope = dot_cfg.get("show_slope", False)

                fn_ad = eval(f"lambda x: {content}")

                # Clear previous
                if build_lines:
                    self.play(*[FadeOut(ln, shift=UP*0.3) for ln in build_lines], run_time=0.3)
