"""
bridge.py — Question Engine → Video Engine Bridge
==================================================
Connects the Axiom Question Engine (TypeScript) to the Orbital Video Engine
(Python). Given a question templateId and seed, it generates the question
and maps it to a video manifest.

Two modes:
  1. JSON file mode:  reads a pre-exported question JSON file
  2. Live mode:       calls the TypeScript engine via Node.js subprocess

The bridge handles the impedance mismatch between the TS and Python worlds:
  - Runs `npx tsx` to execute a small TS shim that generates the question
  - Falls back to reading a JSON file if Node.js or the TS engine aren't available
  - Normalizes the output to Python dict format

Usage
-----
    from bridge import Bridge

    bridge = Bridge()

    # From a JSON file (most reliable)
    manifest = bridge.from_json("sample_questions/examples.json", index=0)

    # From the live TypeScript engine (requires Node.js + Axiom Platform)
    manifest = bridge.from_template("solve-linear-1step", seed=42)

    # Full pipeline with rendering
    video_path = bridge.render("solve-linear-1step", seed=42, output="out.mp4")

Architecture
------------
The TS engine generates questions like:
    {
        "templateId": "solve-linear-1step",
        "questionText": "Solve: 3x + 7 = 22",
        "correctAnswer": "5",
        "solution": "Step 1: ...",
        ...
    }

This JSON is passed to VideoEngine.generate_manifest() to get a Manim manifest,
which is then passed to render.render_manifest() to get a video.
"""

