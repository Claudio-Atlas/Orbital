def _build_geometric(cfg: dict) -> VGroup:
    """
    Geometric constructions.
    cfg keys:
        shapes: [{type, ...params}]
          - polygon: vertices, color, label, fill_opacity
          - circle: center, radius, color, fill_opacity
          - line: start, end, color, dashed
          - angle: vertex, ray1, ray2, label, radius, color
        labels: [{text, position, color}]
    """
    shapes = cfg.get("shapes", [])
    labels = cfg.get("labels", [])
    group = VGroup()

    for s in shapes:
        stype = s.get("type", "line")

        if stype == "polygon":
            verts = s.get("vertices", [[0,0], [2,0], [1,1.7]])
            color = s.get("color", ORBITAL_VIOLET)
            fill_op = s.get("fill_opacity", 0.15)
            poly = Polygon(*[np.array([v[0], v[1], 0]) for v in verts],
                           color=color, fill_opacity=fill_op, stroke_width=2)
            group.add(poly)
            lbl = s.get("label", "")
            if lbl:
                # Label each vertex
                for i, char in enumerate(lbl):
                    if i < len(verts):
                        v = verts[i]
                        # Push label away from centroid
                        cx = sum(vv[0] for vv in verts) / len(verts)
                        cy = sum(vv[1] for vv in verts) / len(verts)
                        dx = v[0] - cx
                        dy = v[1] - cy
                        norm = max((dx**2 + dy**2)**0.5, 0.01)
                        t = MathTex(char, color=color, font_size=28)
                        t.move_to([v[0] + dx/norm*0.4, v[1] + dy/norm*0.4, 0])
                        group.add(t)

        elif stype == "circle":
            cx, cy = s.get("center", [0, 0])
            r = s.get("radius", 1.5)
            color = s.get("color", ORBITAL_CYAN)
            fill_op = s.get("fill_opacity", 0.1)
            circ = Circle(radius=r, color=color, fill_opacity=fill_op, stroke_width=2)
            circ.move_to([cx, cy, 0])
            group.add(circ)

        elif stype == "line":
            st = s.get("start", [-3, 0])
            en = s.get("end", [3, 0])
            color = s.get("color", WHITE)
            dashed = s.get("dashed", False)
            if dashed:
                ln = DashedLine([st[0], st[1], 0], [en[0], en[1], 0],
                                color=color, stroke_width=2)
            else:
                ln = Line([st[0], st[1], 0], [en[0], en[1], 0],
                          color=color, stroke_width=2)
            group.add(ln)

        elif stype == "angle":
            vertex = s.get("vertex", [0, 0])
            ray1   = s.get("ray1", [2, 0])
            ray2   = s.get("ray2", [1, 1.7])
            label  = s.get("label", "")
            radius = s.get("radius", 0.6)
            color  = s.get("color", GEOM_TEAL)

            v = np.array([vertex[0], vertex[1], 0])
            r1 = np.array([ray1[0], ray1[1], 0])
            r2 = np.array([ray2[0], ray2[1], 0])

            l1 = Line(v, r1, color=WHITE, stroke_width=2)
            l2 = Line(v, r2, color=WHITE, stroke_width=2)
            group.add(l1, l2)

            angle = Angle(l1, l2, radius=radius, color=color)
            group.add(angle)

            if label:
                mid_angle = (angle.get_start() + angle.get_end()) / 2
                direction = mid_angle - v
                norm = max(np.linalg.norm(direction), 0.01)
                lbl = MathTex(label, color=color, font_size=28)
                lbl.move_to(v + direction / norm * (radius + 0.35))
                group.add(lbl)

    # Extra labels
    for lb in labels:
        txt = lb.get("text", "")
        pos = lb.get("position", [0, 0])
        col = lb.get("color", WHITE)
        t = MathTex(txt, color=col, font_size=28)
        t.move_to([pos[0], pos[1], 0])
        group.add(t)

    group.scale(0.85)
    return group


