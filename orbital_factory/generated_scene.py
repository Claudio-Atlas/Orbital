                if prev_sec: cleanup_hc.append(prev_sec)
                if prev_md: cleanup_hc.append(prev_md)
                if prev_lbl: cleanup_hc.append(prev_lbl)
                self.play(*[FadeOut(m) for m in cleanup_hc], run_time=0.3)
