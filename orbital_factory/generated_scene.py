def _make_mobject(step: dict):
    """Create the correct Manim object for a step type."""
    stype   = step.get("type", "math")
    content = step.get("content") or step.get("latex", "")
    label   = step.get("label", "")

    if stype == "math":
        mob = MathTex(content, color=WHITE)
        mob.scale(1.4)
        return mob

    if stype == "text":
        mob = Text(content, color=WHITE, font_size=32)
        mob.scale(0.9)
        return mob

    if stype == "mixed":
        # Wrap in math mode if no $ present
        if "$" not in content:
            content = "$" + content + "$"
        mob = Tex(content, color=WHITE)
        mob.scale(0.9)
        return mob

    if stype == "box":
        inner = MathTex(content, color=WHITE)
        inner.scale(0.85)
        box_rect = SurroundingRectangle(
            inner, color=BOX_BORDER, fill_color=BOX_FILL,
            fill_opacity=0.6, buff=0.35, corner_radius=0.12, stroke_width=2,
        )
        if label:
            lbl = Tex(r"\textbf{" + label + "}", color=LABEL_COLOR)
            lbl.scale(0.7)
            lbl.next_to(box_rect, UP, buff=0.15).align_to(box_rect, LEFT).shift(RIGHT * 0.2)
            return VGroup(box_rect, lbl, inner)
        return VGroup(box_rect, inner)

    if stype == "diagram":
        diagram_cfg = step.get("diagram", {})
        kind = diagram_cfg.get("kind", "number_line_cut")
        builder = DIAGRAM_BUILDERS.get(kind)
        if builder:
            return builder(diagram_cfg)
        return Text(f"[diagram: {kind}]", color=GREY, font_size=28)

    if stype == "graph":
        graph_cfg = step.get("graph", {})
        return _build_graph(graph_cfg)

    if stype == "geometric":
        geom_cfg = step.get("geometric", {})
        return _build_geometric(geom_cfg)

    # "transform" and "threed" are handled specially in construct()
    # but we provide a fallback static mob for safety
    if stype == "transform":
        from_latex = step.get("transform", {}).get("from_latex", content)
        mob = MathTex(from_latex, color=WHITE)
        mob.scale(1.4)
        return mob

    if stype == "threed":
        mob = Text("[3D scene]", color=GREY, font_size=28)
        return mob

    # fallback
    mob = MathTex(content, color=WHITE)
    mob.scale(1.4)
    return mob


