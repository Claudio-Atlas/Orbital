def _build_number_line_cut(cfg: dict) -> VGroup:
    """
    Number line with a Dedekind cut.
    cfg keys:
        x_range: [min, max, step]  (default [-3, 3, 1])
        cut_at:  float             (where the cut happens)
        left_label:  str           (label for left region, e.g. "A")
        right_label: str           (label for right region, e.g. "B")
        cut_label:   str           (label at the cut point, e.g. "\\\\sqrt{2}")
        show_point:  bool          (draw dot at cut point, default True)
    """
    x_range   = cfg.get("x_range", [-4, 4, 1])
    cut_at    = cfg.get("cut_at", 0)
    left_lbl  = cfg.get("left_label", "A")
    right_lbl = cfg.get("right_label", "B")
    cut_lbl   = cfg.get("cut_label", "")
    show_pt   = cfg.get("show_point", True)

    nl = NumberLine(
        x_range=x_range,
        length=10,
        include_numbers=True,
        include_tip=True,
        font_size=24,
        color=WHITE,
    )

    group = VGroup(nl)

    # Colored regions (translucent rectangles behind the line)
    left_start = nl.n2p(x_range[0])[0]
    cut_x      = nl.n2p(cut_at)[0]
    right_end  = nl.n2p(x_range[1])[0]
    line_y     = nl.get_center()[1]

    region_h = 0.5
    if cut_x - left_start > 0.1:
        left_rect = Rectangle(
            width=cut_x - left_start, height=region_h,
            fill_color=CUT_LEFT, fill_opacity=0.35,
            stroke_width=0,
        )
        left_rect.move_to([(left_start + cut_x) / 2, line_y, 0])
        group.add(left_rect)

        ll = MathTex(left_lbl, color=CUT_LEFT, font_size=36)
        ll.next_to(left_rect, UP, buff=0.3)
        group.add(ll)

    if right_end - cut_x > 0.1:
        right_rect = Rectangle(
            width=right_end - cut_x, height=region_h,
            fill_color=CUT_RIGHT, fill_opacity=0.35,
            stroke_width=0,
        )
        right_rect.move_to([(cut_x + right_end) / 2, line_y, 0])
        group.add(right_rect)

        rl = MathTex(right_lbl, color=CUT_RIGHT, font_size=36)
        rl.next_to(right_rect, UP, buff=0.3)
        group.add(rl)

    # Cut point
    if show_pt:
        dot = Dot(nl.n2p(cut_at), color=CUT_POINT, radius=0.1)
        group.add(dot)

    if cut_lbl:
        cl = MathTex(cut_lbl, color=CUT_POINT, font_size=36)
        cl.next_to(nl.n2p(cut_at), DOWN, buff=0.35)
        group.add(cl)

    # Dashed vertical line at cut
    dash = DashedLine(
        start=nl.n2p(cut_at) + UP * 0.8,
        end=nl.n2p(cut_at) + DOWN * 0.2,
        color=CUT_POINT, stroke_width=2,
    )
    group.add(dash)

    group.scale(0.75)
    return group


def _build_set_containment(cfg: dict) -> VGroup:
    """
    Nested or overlapping circles.
    cfg keys:
        sets: list of {label, radius, position, color}
        — position is [x, y] offset from center
    """
    sets = cfg.get("sets", [
        {"label": "A", "radius": 1.5, "position": [-0.5, 0], "color": "#4F46E5"},
        {"label": "B", "radius": 1.5, "position": [0.5, 0], "color": "#F97316"},
    ])

    group = VGroup()
    for s in sets:
        circ = Circle(
            radius=s.get("radius", 1.5),
            color=s.get("color", WHITE),
            fill_opacity=0.15,
            stroke_width=2,
        )
        pos = s.get("position", [0, 0])
        circ.move_to([pos[0], pos[1], 0])
        lbl = MathTex(s.get("label", ""), color=s.get("color", WHITE), font_size=36)
        lbl.move_to(circ.get_center() + UP * (s.get("radius", 1.5) + 0.3))
        group.add(circ, lbl)

    group.scale(0.8)
    return group


def _build_sequence_list(cfg: dict) -> VGroup:
    """
    Show a sequence like  r₁, r₂, r₃, ...  with optional highlighting.
    cfg keys:
        items:     list of str (LaTeX)  e.g. ["r_1", "r_2", "r_3", "\\\\dots"]
        highlight: list of int (indices to highlight)
        label:     str (optional label above)
    """
    items     = cfg.get("items", ["r_1", "r_2", "r_3", "\\\\dots"])
    highlight = cfg.get("highlight", [])
    label     = cfg.get("label", "")

    parts = []
    for i, item in enumerate(items):
        color = SEQ_HIGHLIGHT if i in highlight else WHITE
        m = MathTex(item, color=color, font_size=40)
        parts.append(m)
        if i < len(items) - 1:
            comma = MathTex(",", color=GREY, font_size=40)
            parts.append(comma)

    row = VGroup(*parts).arrange(RIGHT, buff=0.2)

    group = VGroup(row)
    if label:
        lbl = Tex(label, color=LABEL_COLOR, font_size=32)
        lbl.next_to(row, UP, buff=0.4)
        group.add(lbl)

    group.scale(0.85)
    return group


def _build_table(cfg: dict) -> VGroup:
    """
    Simple table/grid.
    cfg keys:
        headers: list of str
        rows:    list of list of str
        highlight_cells: list of [row, col] to highlight
    """
    headers = cfg.get("headers", [])
    rows    = cfg.get("rows", [])
    hl      = cfg.get("highlight_cells", [])

    all_rows = []
    if headers:
        all_rows.append(headers)
    all_rows.extend(rows)

    # Build as MathTable
    entries = []
    for r, row in enumerate(all_rows):
        row_entries = []
        for c, cell in enumerate(row):
            m = MathTex(cell, font_size=30)
            # Highlight header row
            if r == 0 and headers:
                m.set_color(LABEL_COLOR)
            # Highlight specific cells
            for hr, hc in hl:
                adj_r = hr + (1 if headers else 0)
                if r == adj_r and c == hc:
                    m.set_color(SEQ_HIGHLIGHT)
            row_entries.append(m)
        entries.append(row_entries)

    # Use VGroup grid layout
    group = VGroup()
    cell_w = 1.4
    cell_h = 0.6
    for r, row in enumerate(entries):
        for c, entry in enumerate(row):
            x = (c - len(row) / 2 + 0.5) * cell_w
            y = -r * cell_h
            entry.move_to([x, y, 0])
            bg = Rectangle(
                width=cell_w - 0.05, height=cell_h - 0.05,
                fill_color=TABLE_HEADER if (r == 0 and headers) else TABLE_CELL,
                fill_opacity=0.4,
                stroke_color=GREY_D, stroke_width=1,
            )
            bg.move_to(entry.get_center())
            group.add(bg, entry)

    group.scale(0.8)
    return group


DIAGRAM_BUILDERS = {
    "number_line_cut": _build_number_line_cut,
    "set_containment": _build_set_containment,
    "sequence_list":   _build_sequence_list,
    "table":           _build_table,
}


