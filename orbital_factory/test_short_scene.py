def _build_graph_zone_c(cfg: dict) -> VGroup:
    """
    Build a function graph VGroup sized to fill Zone C (dominant visual).
    The graph axes span GRAPH_WIDTH × GRAPH_HEIGHT Manim units before scaling.
    Positioning (move_to) is handled by the scene — this returns the VGroup only.
    """
    import numpy as np

    kind        = cfg.get("kind", "function_plot")
    x_range     = cfg.get("x_range", [-5, 5, 1])
    y_range     = cfg.get("y_range", [-5, 5, 1])
    functions   = cfg.get("functions", [])
    points_cfg  = cfg.get("points", [])
    shaded      = cfg.get("shaded_area", None)
    tangent     = cfg.get("tangent", None)
    show_grid   = cfg.get("show_grid", False)
    axis_labels = cfg.get("axis_labels", ["x", "y"])

    default_colors = [ORBITAL_CYAN, GRAPH_ORANGE, GRAPH_PINK, NEON_GREEN, ORBITAL_VIOLET]

    if show_grid:
        axes = NumberPlane(
            x_range=x_range, y_range=y_range,
            x_length=GRAPH_WIDTH, y_length=GRAPH_HEIGHT,
            background_line_style={"stroke_color": DIM_GRAY, "stroke_opacity": 0.5},
        )
    else:
        axes = Axes(
            x_range=x_range,
            y_range=y_range,
            x_length=GRAPH_WIDTH,
            y_length=GRAPH_HEIGHT,
            axis_config={
                "color": WHITE,
                "stroke_width": 2,
                "include_numbers": True,
                "font_size": 24,
                "decimal_number_config": {"num_decimal_places": 0},
            },
            tips=True,
        )

    group = VGroup(axes)

    # Axis labels
    if axis_labels and len(axis_labels) >= 2:
        try:
            x_lbl = axes.get_x_axis_label(axis_labels[0], direction=RIGHT)
            y_lbl = axes.get_y_axis_label(axis_labels[1], direction=UP)
            group.add(x_lbl, y_lbl)
        except Exception:
            pass

    # Plot functions
    plotted_fn_objects = []
    for idx, fn_cfg in enumerate(functions):
        expr_str = fn_cfg.get("expr", "lambda x: x")
        color    = fn_cfg.get("color", default_colors[idx % len(default_colors)])
        dashed   = fn_cfg.get("dashed", False)
        lw       = fn_cfg.get("stroke_width", 3.5)
        try:
            fn_obj = eval(expr_str)  # Input controlled by our pipeline
            if dashed:
                graph_curve = DashedVMobject(
                    axes.plot(fn_obj, color=color, stroke_width=lw), num_dashes=40
                )
            else:
                graph_curve = axes.plot(fn_obj, color=color, stroke_width=lw)
            plotted_fn_objects.append(fn_obj)
            group.add(graph_curve)

            lbl_txt = fn_cfg.get("label", "")
            if lbl_txt:
                x_lbl_pt = x_range[1] * 0.55
                try:
                    y_lbl_pt = fn_obj(x_lbl_pt)
                    lbl_mob = MathTex(lbl_txt, color=color, font_size=30)
                    lbl_mob.move_to(axes.c2p(x_lbl_pt, y_lbl_pt) + UP * 0.45 + RIGHT * 0.35)
                    group.add(lbl_mob)
                except Exception:
                    pass
        except Exception:
            pass

    # Shaded area
    if shaded and plotted_fn_objects:
        fi    = shaded.get("func_index", 0)
        fi2   = shaded.get("func_index_2", None)
        sr    = shaded.get("x_range", [x_range[0], x_range[1]])
        sc    = shaded.get("color", ORBITAL_CYAN)
        so    = shaded.get("opacity", 0.3)
        try:
            if fi < len(plotted_fn_objects):
                top_fn = plotted_fn_objects[fi]
                top_graph = axes.plot(top_fn, color=sc)
                if fi2 is not None and fi2 < len(plotted_fn_objects):
                    bot_fn    = plotted_fn_objects[fi2]
                    bot_graph = axes.plot(bot_fn, color=sc)
                    area = axes.get_area(top_graph, x_range=sr, color=sc, opacity=so,
                                         bounded_graph=bot_graph)
                else:
                    area = axes.get_area(top_graph, x_range=sr, color=sc, opacity=so)
                group.add(area)
        except Exception:
            pass

    # Tangent line
    if tangent and plotted_fn_objects:
        at_x  = tangent.get("at_x", 0)
        t_len = tangent.get("length", 3.5)
        t_col = tangent.get("color", NEON_GREEN)
        try:
            fn_obj = plotted_fn_objects[tangent.get("func_index", 0)]
            dx     = 1e-5
            slope  = (fn_obj(at_x + dx) - fn_obj(at_x - dx)) / (2 * dx)
            y0     = fn_obj(at_x)
            half   = t_len / 2
            x1, y1 = at_x - half, y0 - slope * half
            x2, y2 = at_x + half, y0 + slope * half
            tline  = Line(axes.c2p(x1, y1), axes.c2p(x2, y2),
                          color=t_col, stroke_width=3)
            pt_dot = Dot(axes.c2p(at_x, y0), color=t_col, radius=0.1)
            group.add(tline, pt_dot)
        except Exception:
            pass

    # Points
    for pt_cfg in points_cfg:
        px, py = pt_cfg.get("x", 0), pt_cfg.get("y", 0)
        pcol   = pt_cfg.get("color", CUT_POINT)
        pd     = Dot(axes.c2p(px, py), color=pcol, radius=0.1)
        group.add(pd)
        plbl = pt_cfg.get("label", "")
        if plbl:
            lm = MathTex(plbl, color=pcol, font_size=28)
            lm.next_to(pd, UR, buff=0.15)
            group.add(lm)

    return group


