"""
Orbital Scene Generator v2 — Proof, Text & Diagram Support
============================================================
Step types:
  - "text"    → Tex (LaTeX text mode)
  - "math"    → MathTex (LaTeX math mode)  [default / backward compat]
  - "mixed"   → Tex with inline $...$ math
  - "box"     → labeled theorem/claim/key-fact box
  - "diagram" → visual diagrams (number_line_cut, set_containment, sequence_list, table)

Display modes:
  - "replace" → fade out previous, write new  [default]
  - "build"   → lines accumulate on screen (scroll when full)

Diagram sub-kinds (in step["diagram"]):
  - number_line_cut:  Number line split at a point with colored regions
  - set_containment:  Nested/overlapping circles (Venn-style)
  - sequence_list:    Show a sequence with ellipsis (for diagonal arguments)
  - table:            Rows/columns grid

Usage:
    from scene_v2 import create_synced_scene_v2
    create_synced_scene_v2(manifest, "scene.py", intro_duration=2.0)
"""

import json
import os


MAX_BUILD_LINES = 5
TOP_Y = 3.0
LINE_SPACING = 0.85


def create_synced_scene_v2(manifest: list, output_path: str, intro_duration: float = 2.0):
    """
    Write a Manim scene .py file that handles all step types.
    """

    manifest_json = json.dumps(manifest, indent=8)

